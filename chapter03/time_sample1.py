# ────────────────────────────────────────────────────────────
# 예제 4: time.strftime() — 원하는 형식으로 시간 출력
# ────────────────────────────────────────────────────────────
# 서식 코드 정리:
#   %Y = 4자리 연도    %m = 2자리 월     %d = 2자리 일
#   %H = 시(24시간)    %M = 분           %S = 초
#   %A = 영문 요일     %p = AM/PM
# ────────────────────────────────────────────────────────────
print("=" * 50)
print("☞ 예제 4: time.strftime()")
print("=" * 50)

now = time.localtime()

# 형식 1: 2026-03-16 14:30:45
result1 = time.strftime("%Y-%m-%d %H:%M:%S", now)
print(f"형식1: {result1}")

# 형식 2: 2026년 03월 16일
result2 = time.strftime("%Y년 %m월 %d일", now)
print(f"형식2: {result2}")

# 형식 3: 14시 30분 45초 (Monday)
result3 = time.strftime("%H시 %M분 %S초 (%A)", now)
print(f"형식3: {result3}")
print()

